<?php
// Chapter2 PHPの基礎　Section16 演算子
// 加算子/減算子 3　演算子を使用する

$count = 0;

print $count++; // 0
print "<br>";
print $count; // 1
print "<br>";

$count = 0;

print ++$count; // 1
print "<br>";
print $count; // 1
print "<br>";

$count = 3;

print $count--; // 3
print "<br>";
print $count; // 2
print "<br>";

$count = 3;

print --$count; // 2
print "<br>";
print $count; // 2
print "<br>";
